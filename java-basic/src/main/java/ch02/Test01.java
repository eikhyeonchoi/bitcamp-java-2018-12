//자바 리터럴 literal - 자바 코드로 값을 표현하는 방법  1
//
package ch02;

public class Test01 {
  public static void main(String[] args){
    //정수 78을 출력
    //10진수
    System.out.println(78);
    
    //8진
    System.out.println(0116);
    
    //16진수
    System.out.println(0x4e);
    
    //2진수
    System.out.println(0e1001110);
    
    //부동소수점 출력 12.345
     System.out.println(12.345);
     
    //1.2345를 12.345
    System.out.println(1.2345e1);
    
    //0.12345를 12.345
    System.out.println(0.12345e2);
    
    //123.45를 12.345
    System.out.println(123.45e-1);
    
    //true
    System.out.println(true);
    
    //false
    System.out.println(false);
    
    //문자출력 0x61을 출력했을때 a로
    System.out.println((char)0x61);
    
    //97값을 출력했을때 a로
    System.out.println((char)97);
    
    //''을 사용해여 a출력
    System.out.println('a');
    
    //문자열을 출력 hello
    System.out.println("hello");
    
    //문자열을 출력 안녕하세요
    System.out.println("안녕하세요");
    
    
    
    
    
    
    
  }
}






